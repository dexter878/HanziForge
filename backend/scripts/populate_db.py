def generate_pinyin_marks(text: str) -> str:
    """Генерирует пиньинь с тоновыми знаками (мандаринский диалект).
    Пример: '你好' -> 'nǐ hǎo'
    """
    if not HAS_PYPINYIN:
        return ""
    result = pinyin(text, style=Style.TONE, heteronym=False)
    return " ".join(item[0] for item in result)


def generate_pinyin_numbers(text: str) -> str:
    """Генерирует пиньинь с номерами тонов (мандаринский диалект).
    Пример: '你好' -> 'ni3 hao3'
    """
    if not HAS_PYPINYIN:
        return ""
    result = pinyin(text, style=Style.TONE3, heteronym=False)
    return " ".join(item[0] for item in result)


def generate_sentence_pinyin(text: str) -> str:
    """Генерирует пиньинь для предложения, сохраняя пунктуацию.
    Пример: '你好！' -> 'nǐ hǎo!'
    """
    if not HAS_PYPINYIN:
        return ""
    import re
    # Убираем пунктуацию из текста для пиньинь, потом добавляем обратно
    # Заменяем китайскую пунктуацию на латинскую
    punct_map = {'。': '.', '！': '!', '？': '?', '，': ',', '、': ',',
                 '；': ';', '：': ':', '（': '(', '）': ')', '"': '"',
                 '"': '"', ''': "'", ''': "'", '《': '<', '》': '>',
                 '…': '...'}
    
    # Собираем чистый текст и позиции пунктуации
    clean_chars = []
    trailing_punct = []
    
    for ch in text:
        if ch in punct_map:
            if clean_chars:
                trailing_punct.append(punct_map[ch])
            else:
                trailing_punct.append(punct_map[ch])
        elif ch in '.,!?;:()"<>\'-':
            trailing_punct.append(ch)
        else:
            if trailing_punct:
                clean_chars.append(('punct', ''.join(trailing_punct)))
                trailing_punct = []
            clean_chars.append(('char', ch))
    
    # Собираем только иероглифы для пиньинь
    hanzi_text = ''.join(ch for typ, ch in clean_chars if typ == 'char')
    if not hanzi_text.strip():
        return ""
    
    py_result = pinyin(hanzi_text, style=Style.TONE, heteronym=False)
    
    # Собираем обратно с пунктуацией
    py_index = 0
    parts = []
    for typ, val in clean_chars:
        if typ == 'char':
            if py_index < len(py_result):
                parts.append(py_result[py_index][0])
                py_index += 1
        else:  # punct
            # Приклеиваем пунктуацию к последнему слову
            if parts:
                parts[-1] = parts[-1] + val
            else:
                parts.append(val)
    
    if trailing_punct:
        if parts:
            parts[-1] = parts[-1] + ''.join(trailing_punct)
        else:
            parts.append(''.join(trailing_punct))
    
    return ' '.join(parts)

